// Boolean values
let a = true;
let b = false;

// AND operation
let andResult = a && b;

// OR operation
let orResult = a || b;

// NOT operation
let notResult = !a;

// Display results
console.log("Value of a:", a);
console.log("Value of b:", b);

console.log("a AND b =", andResult);
console.log("a OR b =", orResult);
console.log("NOT a =", notResult);